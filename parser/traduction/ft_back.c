#include "../../include/minishell.h"

int		ft_back(char *tmp, int a)
{
	int i;

	i = 0;
	while (tmp && tmp[a])
	{
		while (tmp[a] == '\\')
		{
			i++;
			a++;
		}
		if (tmp[a] == '"')
			return (i);
		a++;
	}
	return (i);
}
